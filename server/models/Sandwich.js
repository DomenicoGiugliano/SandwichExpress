const mongoose = require("mongoose");

const sandwichSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
    unique: true,
  },
  description: {
    type: String,
    required: true,
  },
  price: {
    type: Number,
    required: true,
  },
  img: {
    type: String,
    required: true,
  },
  categories: [String],
});

module.exports = mongoose.model("Sandwich", sandwichSchema);
